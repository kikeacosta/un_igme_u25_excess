# Peru mortality data
# ~~~~~~~~~~~~~~~~~~~
# files from OSF (Version 1) as of 15 March 2021 

# load data
db_pe <- 
  read_xlsx(unzip(here("Data", "Peru", "peru_deaths.zip"),
                  "SINADEF_DATOS_ABIERTOS_19032021.xlsx"), skip = 2)

# data wrangling
db_pe2 <- db_pe %>% 
  select(Sex = SEXO,
         Age = EDAD, 
         Year = AÑO,
         unit_age = 'TIEMPO EDAD') %>% 
  mutate(Sex = recode(Sex,
                      "MASCULINO" = "m",
                      "FEMENINO" = "f"))

db_pe3 <- 
  db_pe2 %>%
  mutate(Sex = case_when(Sex == "m" | Sex == "f" ~ Sex,
                         TRUE ~ "UNK"),
         Age = ifelse(unit_age == "AÑOS", Age, "0"),
         Age = as.integer(Age),
         Age = case_when(Age > 100 ~ "100",
                         is.na(Age) | Age == 999 ~ "UNK", 
                         TRUE ~ as.character(Age)),
         Year = as.double(Year)) %>% 
  group_by(Year, Sex, Age) %>% 
  summarise(Deaths = n()) %>% 
  ungroup() %>% 
  filter(Year >= 2017) %>% 
  mutate(Country = "Peru") %>% 
  select(Country, Year, Sex, Age, Deaths)

# all sex and all age Peru and Mexico
db_pe_mx <- 
  bind_rows(db_pe3,
            db_mx) %>% 
  arrange(Country, Year, Sex, suppressWarnings(as.numeric(Age)))

unique(db_pe_mx$Age)

db_pe_mx_all_ages <- 
  db_pe_mx %>% 
  group_by(Country, Year, Sex) %>% 
  summarise(Deaths = sum(Deaths)) %>% 
  ungroup() %>% 
  mutate(Age = "TOT") %>% 
  filter(Sex != "UNK")

db_pe_mx_all_sex <- 
  db_pe_mx %>% 
  group_by(Country, Year, Age) %>% 
  summarise(Deaths = sum(Deaths)) %>% 
  ungroup() %>% 
  mutate(Sex = "t") %>% 
  filter(Age != "UNK")

db_pe_mx_all_sex_age <- 
  db_pe_mx %>% 
  group_by(Country, Year) %>% 
  summarise(Deaths = sum(Deaths)) %>% 
  ungroup() %>% 
  mutate(Sex = "t",
         Age = "TOT")

db_pe_mx2 <- 
  db_pe_mx %>% 
  filter(Age != "UNK",
         Sex != "UNK") %>% 
  bind_rows(db_pe_mx_all_ages,
            db_pe_mx_all_sex,
            db_pe_mx_all_sex_age) %>% 
  arrange(Country, Year, Sex, suppressWarnings(as.numeric(Age)))

# saving annual deaths in Mexico and Peru
write_csv(db_pe_mx2, "Output/pe_mx_annual_deaths.csv")

